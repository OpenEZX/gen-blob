int drawline_color(int x0, int y0, int x1, int y1, int color)
{
	int c = pal_fg;
	int ret;

	pal_fg = color;
	ret = drawline(x0, y0, x1, y1);
	pal_fg = c;
	return ret;
}

int drawline(int x0, int y0, int x1, int y1)
{
        int i, tempo;
        unsigned char *p;

        // test for the line not being horizontal or vertical
        if ((x0 != x1) && (y0 != y1)) {	
		return -1;
        }

        if (x0 == x1)	// this line is horizontal
        {
                if (y0 > y1) {			// make sure that the smaller value is in y0
                        tempo = y0;
                        y0 = y1;
                        y1 = tempo;
                }

                for (i=y0; i<=y1;) {		// col
                        p = &fb_8bit->pixel[x0][i];
                        *p = pal_fg;
                        i = i + 1;
                }

        } else {	// this line is vertical
		if (x0 > x1) {			// make sure that the smaller value is in x0
			tempo = x0;
                        x0 = x1;
                        x1 = tempo;
                }

                for (i=x0; i<=x1;) {   		// col
                        p = &fb_8bit->pixel[i][y0];
                        *p = pal_fg;
                        i = i + 1;
                }
        }
	
	return 1;
}

void Delay(int uSec)
{
        int delaytime;
        delaytime = (int)(uSec*3.6864);
        OSCR = 0;
        while( OSCR > 50)
                ;
        while((OSCR) < delaytime)
                ;
}

int checkkey()
{
        unsigned long data, temp;
        unsigned long timeout;

        GAFR0_L &= 0xFFFFFCFF;
        GPDR0 &= 0xFFFFFFEF;

        //a17400 debug, if no key down, directly return bad
        if ((GPLR0 & 0X10))
                return 0;
begin:
        SerialOutputString("begin scan gpio4\n ");
        //printlcd("begin scan gpio4\n ");

        timeout = 0x10000000;
        while((data=(GPLR0 & 0X10))) {
                if(--timeout==0)
                        goto bad;
        }
        Delay(1000);
        temp = GPLR0 & 0x10;
        if(data != temp)
                goto begin;

        SerialOutputString("key down\n ");
        //printlcd("key down\n ");

        timeout = 0x10000000;
        while(!(data=(GPLR0 & 0X10))) {
                if(--timeout==0)
                        goto bad;
        }
        SerialOutputString("key up\n ");
        //	printlcd("key up\n ");

        return 1;
bad:
        SerialOutputString("not detect gpio4\n ");
        //	printlcd("not detect gpio4\n ");
        return 0;
}

