static int transparence = 0;

int putcToFB_8bit(char c, unsigned int row, unsigned int col)
{

        unsigned char *font;
        unsigned char *pix;
        unsigned int f;
        int r;

        if ((row > 15) || (col > 23)) {                                          /* check if row/col in range */
                return -1;
        };

        /* gen pointer into fb at upper left corner of char cell */
        if ((row * 18) < (LCD_Y-30)) {
                pix = &fb_8bit->pixel[((row*18)+1)][col*10];
        } else {
                row = 15;
                pix = &fb_8bit->pixel[((row*18)+1)][col*10];
        };

        font = &font9x16[((c & 0xff) << 4)];

        for (r=0; r<18; r++) {
                if ((r == 0) || (r == 17)) {
                        f = 0;
                } else {
                        f = *font++;
                };

			/* Added transparence support by Alex */
			if (transparence == 0) {
                *pix++ = pal_bg;
                *pix++ = ((f>>7) & 0x1) ? (pal_fg) : pal_bg;
                *pix++ = ((f>>6) & 0x1) ? (pal_fg) : pal_bg;
                *pix++ = ((f>>5) & 0x1) ? (pal_fg) : pal_bg;
                *pix++ = ((f>>4) & 0x1) ? (pal_fg) : pal_bg;
                *pix++ = ((f>>3) & 0x1) ? (pal_fg) : pal_bg;
                *pix++ = ((f>>2) & 0x1) ? (pal_fg) : pal_bg;
                *pix++ = ((f>>1) & 0x1) ? (pal_fg) : pal_bg;
                *pix++ = ((f>>0) & 0x1) ? (pal_fg) : pal_bg;
                *pix++ = (pal_bg);
			}
			else {
				int i;
                pix++;
				for (i = 8; i > 0; i--) {
					if ((f>>(i-1)) & 0x1) 
						*pix = pal_fg;
					pix++;
				}
           	   pix++;
			}
                pix += (LCD_X-10);
        }

        return 0;
}

void set_transparence(int on)
{
	transparence = on;
}

