void printlcd(char *cntrl_string)
{

        int length, i;
        char c;

        c = 0;

        length = strlen(cntrl_string);

        for(i=0; i<length; i++) {
                c = cntrl_string[i];
                if(c == 0xa) {
                        if(lcd_row < 15) {
                                lcd_row++;
                                lcd_col = 0;
                                putcToFB_8bit(0,lcd_row,lcd_col);
                        } else {
                                lcd_row = 15;
                                scroll_line();
                                lcd_col = 0;
                                putcToFB_8bit(0,lcd_row,lcd_col);
                        }
                } else {
                        putcToFB_8bit(c,lcd_row,lcd_col);
                        lcd_col++;
                }
        }
        //cacheflush();
}

