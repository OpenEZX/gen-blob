/*
* fast screen cleaner
*/
void clrScreen()
{
        int i;
        unsigned char *p;

        p = &fb_8bit->pixel[0][0]; /* get pointer to start of frame buffer */
        /* clear all of buffer */
        for (i=0; i<(LCD_X*LCD_Y); i++) {
                *p++ = pal_bg;
        };
        lcd_row = 0;
        lcd_col = 0;
}
/*****************************************************************************/



/*
* fast char row cleaner
*/
void clrCharRow(int row)
{

        int i;
        unsigned char *p;

        p = &fb_8bit->pixel[0][0] + (row * 18 * LCD_X) ; /* get pointer to start of row in frame buffer */

        /* clear a row of char in frame buffer */
        for (i=0; i<(((LCD_X)*18)); i++) {
                *p++ = pal_bg;
        };
}
/*****************************************************************************/




/*
* Fast scroll up one line
*/
void scroll_line()
{

        int *f,*t,i;

        f = (int *) (&fb_8bit->pixel[0][0] + (1 * 18 * LCD_X)); /* get pointer to start of row 1 in frame buffer */
        t = (int *) (&fb_8bit->pixel[0][0] + (0 * 18 * LCD_X)); /* get pointer to start of row 0 in frame buffer */

        /* move all rows up one */
        for (i=((LCD_X*18*1)/4); i<((LCD_X*18*16)/4); i++) {
                *t++ = *f++;
        };
        /* clear new row */
        clrCharRow(15);
}
